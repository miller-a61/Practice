using System;
class oddNumbers
{
    static void Main(string[] args)
    {
        int i;
        for (i = 1; i < 100; i = i + 2)
        {
            Console.WriteLine(i);
        }
    }
}

/*
 * ODD NUMBERS
CHALLENGE DESCRIPTION:


Print the odd numbers from 1 to 99.

INPUT SAMPLE:

There is no input for this program.

OUTPUT SAMPLE:

Print the odd numbers from 1 to 99, one number per line. 
*/